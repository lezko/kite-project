package com.lezko.kite.components;

import java.awt.*;

public class MyComponent {

    private int width, height;

    public void setSize(int width, int height) {
        this.width = width;
        this.height = height;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }
}
